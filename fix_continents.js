// 更新所有城市的大洲
let updated = 0;
cities.cities.forEach(city => {
  const mappedContinent = continentMap[city.name];
  if (mappedContinent) {
    if (city.continent !== mappedContinent) {
      city.continent = mappedContinent;
      updated++;
    }
  }
});

// 处理重复城市：对于美国圣地亚哥之外的其他重复，尝试从其他映射中确定
// 在这种情况下，我们已经通过上面的映射处理了
// 但为了清晰起见，让我们检查智利圣地亚哥是否正确设置
const santiagoCities = cities.cities.filter(c => c.name === '圣地亚哥');
santiagoCities.forEach(city => {
  if (city.country === '智利') {
    city.continent = '南美洲';
  } else if (city.country === '美国') {
    city.continent = '北美洲';
  }
});

// 保存更新
