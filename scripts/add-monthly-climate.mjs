let src = readFileSync(FILE, "utf8");

let updated = 0;
let skipped = 0;

for (const [idStr, data] of Object.entries(MONTHLY)) {
  const id = Number(idStr);
  // Match the line for this city ID in CITY_CLIMATE
  // Pattern: id: { ... }
  const re = new RegExp(`(\\b${id}:\\s*\\{[^}]+)(\\})`);
  const match = src.match(re);
  if (!match) {
    console.log(`⚠ City ${id}: not found in CITY_CLIMATE, skipping`);
    skipped++;
    continue;
  }

  const existing = match[0];
  // Check if already has monthly data
  if (existing.includes("monthlyHighC")) {
    // Replace existing monthly data
    const base = existing.replace(/,\s*monthlyHighC:.*$/, "}");
    const newLine = base.replace(/\}$/, `, monthlyHighC: [${data.h}], monthlyLowC: [${data.l}], monthlyRainMm: [${data.r}] }`);
    src = src.replace(existing, newLine);
    updated++;
  } else {
    // Add monthly data
    const newLine = existing.replace(/\}$/, `, monthlyHighC: [${data.h}], monthlyLowC: [${data.l}], monthlyRainMm: [${data.r}] }`);
    src = src.replace(existing, newLine);
    updated++;
  }
}

