def update_salaries():
    """Load cities.json, apply researched salaries, write back."""
    json_path = os.path.join(os.path.dirname(__file__), 'public', 'data', 'cities.json')
    with open(json_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    updated_direct = 0
    updated_interp = 0

    for city in data['cities']:
        cid = city['id']
        if cid in SALARY_DATA:
            salaries = SALARY_DATA[cid]
            for i, prof in enumerate(PROFS):
                city['professions'][prof] = salaries[i]
            updated_direct += 1
        else:
            # Should not happen since we have all 100 cities, but fallback
            print(f"WARNING: No data for city ID {cid} ({city['name']})")
            updated_interp += 1

    with open(json_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"\n=== Salary Update Complete ===")
    print(f"Directly updated: {updated_direct} cities")
    if updated_interp:
        print(f"Missing (unchanged): {updated_interp} cities")
    print(f"Total professions updated: {updated_direct * 20}")

    # Verification: sample cities
    print("\n=== Verification Samples ===")
    sample_ids = [1, 2, 3, 4, 7, 16, 49, 32, 52, 30, 72, 82, 101, 102, 106, 117, 110, 114]
    for sid in sample_ids:
        c = next((c for c in data['cities'] if c['id'] == sid), None)
        if not c:
            continue
        profs = c['professions']
        sw = profs['软件工程师']
        doc = profs['医生/医学博士']
        law = profs['律师']
        teach = profs['教师']
        avg = c['averageIncome']
        print(f"  {c['name']:8s} (avg={avg:>6d}): SW={sw:>7,} Doc={doc:>7,} Law={law:>7,} Teach={teach:>6,}  [SW/avg={sw/avg:.2f}x]")


if __name__ == '__main__':
    update_salaries()
